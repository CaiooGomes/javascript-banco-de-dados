//um igual significa atribuição
var numero = 12
const texto = "12"
//dois iguais é uma pergunta sem tipagem
console.log(texto == numero);
//tres iguais verificam se o conteudo é o mesmo e tambem checa a tipagem
console.log(texto === numero);
//dois iguais e uma escramaçao checam se é diferente
console.log(texto !== numero);
